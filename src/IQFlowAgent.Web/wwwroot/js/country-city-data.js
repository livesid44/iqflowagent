/**
 * Get cities for a list of country names.
 * @param {string[]} countries
 * @returns {string[]} sorted deduplicated city list
 */
function getCitiesForCountries(countries) {
  const set = new Set();
  countries.forEach(c => (CITY_MAP[c] || []).forEach(city => set.add(city)));
  return Array.from(set).sort();
}
